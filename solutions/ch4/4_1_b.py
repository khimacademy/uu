'''
4-1. 피자
좋아하는 피자를 최소한 세 가지 생각한 다음 피자 종류를 리스트에 저장하고, for 루프를 써서 피자의 종류를 모두 출력하세요.
- for 루프를 수정해서 단순히 피자 종류만 출력하지 말고 문장을 출력하세요. 예를 들어 "나는 페퍼로니 피자를 좋아합니다." 같은 단순한 문장을 한 줄씩 출력해야 합니다.
- 프로그램 마지막, for 루프 다음에 행을 추가해 당신이 피자를 얼마나 좋아하는지 나타내는 문장을 만드세요. 출력 결과는 당신이 좋아하는 피자에 관한 세 줄 이상의 문장과 "나는 피자를 정말 좋아해요!" 같은 문장이어야 합니다.

Output:
pepperoni
hawaiian
veggie

I really love pepperoni pizza!
I really love hawaiian pizza!
I really love veggie pizza!

I really love pizza!
'''

favorite_pizzas = ['pepperoni', 'hawaiian', 'veggie']

# Print the names of all the pizzas.
for pizza in favorite_pizzas:
    print(pizza)

# Print a sentence about each pizza.
for pizza in favorite_pizzas:
    print('I really love ' + pizza + ' pizza!')

print('\nI really love pizza!')

